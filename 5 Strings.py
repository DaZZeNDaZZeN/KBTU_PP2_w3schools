s = " This is a string "
print(s.lower()) # all letters to lowercase
print(s.upper()) # all letters to uppercase
print(len(s)) # length of s
print(s[1:5]) # Slice from 1 to 4 incluse (This)
print(s.strip()) # Removes trailing whitespaces from both sides
print(s.split()) # splits string by whitespaces
s = s.replace("s", "c") # replaces all occurences of "s" by "c"
print(s)
